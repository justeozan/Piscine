#include <stdio.h>

void ft_swap(int *a, int *b){

    int temp;

    temp = *a;
    *a = *b;
    *b = temp;
}

int main(){
    int a, b;

    a = 4;
    b = 2;
    printf("%d%d", a, b);
    ft_swap(&a, &b);
    printf("\n%d%d", a, b);
    return 0;
}